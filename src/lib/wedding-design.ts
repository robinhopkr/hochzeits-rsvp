export type WeddingTemplateId = (typeof WEDDING_TEMPLATE_OPTIONS)[number]['id']
export type WeddingFontPresetId = (typeof WEDDING_FONT_OPTIONS)[number]['id']

const templateMap = new Map<string, WeddingTemplateOption>(
  WEDDING_TEMPLATE_OPTIONS.map((template) => [template.id, template]),
)
const fontMap = new Map<string, WeddingFontOption>(
  WEDDING_FONT_OPTIONS.map((font) => [font.id, font]),
)

export function isWeddingTemplateId(value: string | null | undefined): value is WeddingTemplateId {
  return typeof value === 'string' && templateMap.has(value)
}

export function isWeddingFontPresetId(
  value: string | null | undefined,
): value is WeddingFontPresetId {
  return typeof value === 'string' && fontMap.has(value)
}

export function getWeddingTemplate(templateId: string | null | undefined): WeddingTemplateOption {
  if (templateId && templateMap.has(templateId)) {
    return templateMap.get(templateId) as WeddingTemplateOption
  }

  return templateMap.get(DEFAULT_WEDDING_TEMPLATE_ID) as WeddingTemplateOption
}

export function getWeddingFontPreset(fontPresetId: string | null | undefined): WeddingFontOption {
  if (fontPresetId && fontMap.has(fontPresetId)) {
    return fontMap.get(fontPresetId) as WeddingFontOption
  }

  return fontMap.get(DEFAULT_WEDDING_FONT_PRESET_ID) as WeddingFontOption
}

export function buildWeddingThemeStyle(input: {
  templateId?: string | null | undefined
  fontPresetId?: string | null | undefined
}): CSSProperties {
  const template = getWeddingTemplate(input.templateId)
  const fontPreset = getWeddingFontPreset(input.fontPresetId)

  return {
    ...template.cssVariables,
    '--font-display-active': fontPreset.displayVar,
    '--font-body-active': fontPreset.bodyVar,
  } as CSSProperties
}
